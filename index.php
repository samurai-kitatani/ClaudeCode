<?php
	$dir = 'images/report/';
	$files = glob($dir . '*.pdf');
	$report_list = [];

	if ($files) {
	    foreach ($files as $file_path) {
	        $filename = basename($file_path);
	        // ファイル名解析: 2025-12-11_1262.pdf
	        if (preg_match('/^(\d{4}-\d{2}-\d{2})_(\d+)\.pdf$/', $filename, $matches)) {
	            $date_raw = $matches[1];
	            $number   = $matches[2];
	            $timestamp = strtotime($date_raw);
	            $date_disp = date('Y.n.j', $timestamp);

	            $report_list[] = [
	                'file_path' => $file_path,
	                'number'    => $number,
	                'date_disp' => $date_disp,
	                'timestamp' => $timestamp
	            ];
	        }
	    }

	    // 新しい順にソート
	    usort($report_list, function($a, $b) {
	        return $b['timestamp'] - $a['timestamp'];
	    });

	    // 最新の3件だけを取り出す
	    $latest_reports = array_slice($report_list, 0, 3);

	    // 出力
	    foreach ($latest_reports as $report) {
	        echo "\t\t\t\t\t<dt><a href=\"{$report['file_path']}\" target=\"_blank\"><img src=\"images/common/icon_pdf.png\" alt =\"icon_pdf\" class=\"width100\"/></a></dt>\n";
	        echo "\t\t\t\t\t<dd><a href=\"{$report['file_path']}\" target=\"_blank\">会報 第{$report['number']}号・第{$report['number']}回 例会</a><br />{$report['date_disp']}</dd>\n";
	    }
	}
